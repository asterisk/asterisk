{
	int x;
	for (x = 0;x < AST_MAX_CONNECTS; x++) {
		if (consoles[x].mute)
			continue;
		if (consoles[x].fd > -1) {
			if (!consoles[x].levels[level]) 
				fdprint(consoles[x].p[1], string);
		}
	}
}

